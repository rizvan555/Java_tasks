package Composition_Computer;

public class Resolution {
    private int en;
    private int boy;

    public Resolution(int en, int boy){
        this.en=en;
        this.boy=boy;
    }

    public int getEn(){
        return en;
    }
    public int getBoy(){
        return boy;
    }

    public void setEn(int en){
        this.en=en;
    }
    public void setBoy(int boy){
        this.boy=boy;
    }
}

